#soal 4.2
#1
Fungsi sort(array):
    Uuntuk menyortir elemen-elemen dari array yang diindeks dalam ascending order (abjad untuk 
    huruf dan angka untuk angka)
#2
Fungsi array_pop($array):
    Untuk memasukkan value ke sebuah array dan disimpan di paling akhir / paling kanan.
#3
Fungsi array_push($array):
    Untuk menyisipkan satu atau lebih elemen ke akhir array.
#4
Fungsi array_shift($array):
    Menghapus elemen pertama pada array.
#5
Fungsi array_unshift($array):
    Menambah value di ujung kiri array.